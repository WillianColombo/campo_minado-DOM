export function colorsTheme(theme) {
    switch (theme) {
        case 'brown':
            return brown = {
                middle: '#B85639',
                dark: '#75331D',
                ligth: '#D2907A'
            }
        case 'dark':
            return dark = {
                middle: '#393437',
                dark: '#1b1619',
                ligth: '#575154'
            }
        case 'ligth':
            return ligth = {
                middle: '#dfdde6',
                dark: '#bebbcd',
                ligth: '#ffffff'
            }
        case 'green':
            return green = {
                middle: '#1ab668',
                dark: '#009046',
                ligth: '#33dc8a'
            }
    }

}
